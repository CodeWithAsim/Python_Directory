number = input("Enter a number : ")
num = int(number)
if num % 2 == 0:
    print("number you have entered is even number !")
elif num % 3 == 0:
    print("number you have entered is multiple of 3 !")
elif num % 4 == 0:
    print("number you have entered is multiple of 4 !")
else:
    print("number you have entered is out of my knowledge !")


